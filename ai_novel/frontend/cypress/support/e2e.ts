// コマンドのインポート
import './commands';

// テスト実行前に一度だけ実行されるコード
beforeEach(() => {
  // テスト実行前にローカルストレージをクリア
  cy.clearLocalStorage();

  // テスト実行前にCookieをクリア
  cy.clearCookies();
});

// 例外を処理するための設定
Cypress.on('uncaught:exception', (err, runnable) => {
  // 一部のサードパーティライブラリのエラーを無視する設定
  // React Hydrationエラーなどを無視する場合に有効
  // 通常はfalseを返さず、テストを失敗させるべきだが、開発環境では必要な場合がある
  return false;
});
